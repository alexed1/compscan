import pytest
from unittest.mock import patch
from src.email_reporter import EmailReporter
from src.models import CompetitorChange


def make_change(name="Competitor A - Homepage", url="https://example.com",
                content="Some content", last_checked="2024-01-14 12:00 UTC"):
    return CompetitorChange(name=name, url=url, content=content, last_checked=last_checked)


@pytest.fixture
def email_reporter():
    return EmailReporter(
        api_key="test_api_key",
        from_email="test@example.com",
        from_name="Test Sender",
        to_emails=["recipient@example.com"]
    )


