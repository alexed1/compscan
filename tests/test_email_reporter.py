# ---------------------------------------------------------------------------

def test_email_reporter_initialization(email_reporter):
    assert email_reporter.from_email == "test@example.com"
    assert email_reporter.from_name == "Test Sender"
    assert email_reporter.to_emails == ["recipient@example.com"]


