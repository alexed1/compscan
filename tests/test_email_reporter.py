def test_send_digest_no_changes_still_sends(email_reporter):
    with patch('resend.Emails.send') as mock_send:
        mock_send.return_value = {"id": "email_123"}
        result = email_reporter.send_digest([], "No changes", "[Test]")

    assert result is True
    mock_send.assert_called_once()
    subject = mock_send.call_args[0][0]['subject']
    assert "No changes detected" in subject
    assert "change(s) detected" not in subject


def test_send_digest_success(email_reporter):
    changes = [make_change("Test Competitor - Homepage", "https://test.com")]

    with patch('resend.Emails.send') as mock_send:
        mock_send.return_value = {"id": "email_123"}
        result = email_reporter.send_digest(changes, "Test analysis", "[Test]")

    assert result is True
    mock_send.assert_called_once()
    call_args = mock_send.call_args[0][0]
    assert call_args['from'] == "Test Sender <test@example.com>"
    assert call_args['to'] == ["recipient@example.com"]
    assert "[Test]" in call_args['subject']
    assert "1 change(s) detected" in call_args['subject']
    assert len(call_args['html']) > 0


def test_send_digest_multiple_recipients(email_reporter):
    email_reporter.to_emails = ["user1@example.com", "user2@example.com", "user3@example.com"]
    changes = [make_change()]

    with patch('resend.Emails.send') as mock_send:
        mock_send.return_value = {"id": "email_123"}
        result = email_reporter.send_digest(changes, "Analysis", "[Test]")

    assert result is True
    assert mock_send.call_count == 3


def test_send_digest_failure(email_reporter):
    changes = [make_change()]

    with patch('resend.Emails.send') as mock_send:
        mock_send.side_effect = Exception("Email sending failed")
        result = email_reporter.send_digest(changes, "Analysis", "[Test]")

    assert result is False


def test_send_digest_subject_plural(email_reporter):
    changes = [make_change("A - Home"), make_change("B - Pricing", "https://b.com")]

    with patch('resend.Emails.send') as mock_send:
        mock_send.return_value = {"id": "x"}
        email_reporter.send_digest(changes, "Analysis", "[Alert]")

    subject = mock_send.call_args[0][0]['subject']
    assert "2 change(s) detected" in subject


def test_send_digest_partial_failure_returns_false(email_reporter):
    """First recipient succeeds, second raises — result should be False."""
    email_reporter.to_emails = ["ok@example.com", "fail@example.com"]
    changes = [make_change()]

    with patch('resend.Emails.send') as mock_send:
        mock_send.side_effect = [{"id": "ok"}, Exception("fail")]
        result = email_reporter.send_digest(changes, "Analysis", "[Test]")

    assert result is False
